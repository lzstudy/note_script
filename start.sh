ROOT_DIR=$PWD/../


########################################################################################################
# init 
########################################################################################################
function script_init()
{
    cp ./script/deploy $ROOT_DIR                                               # 拷贝部署脚本到根目录
    cp ./script/cport $ROOT_DIR                                                # 拷贝端口号信息到根目录
}

########################################################################################################
# 程序入口
########################################################################################################
function start_entry()
{
    script_init                                                                 # 初始化脚本
}

start_entry $@
